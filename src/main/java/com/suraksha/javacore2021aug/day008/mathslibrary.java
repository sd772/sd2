public class mathslibrary {

    public static Integer ADDITION(Integer firstNum, Integer secNum) {
        System.out.println("2 Integer Addition Static Function");
        return firstNum + secNum;
    }

    public static Integer SUBTRACTION(Integer firstNum, Integer secNum) {
        System.out.println("2 Integer Subtraction Static Function");
        return firstNum + secNum;
    }

    public static Integer MULTIPLICATION(Integer firstNum, Integer secNum) {
        System.out.println("2 Integer Multiplication Static Function");
        return firstNum + secNum;

    }

    public static Double DIVISION(Integer firstNum, Integer secNum) {
        System.out.println("2 Integer Division Static Function");
        return (double)firstNum + secNum;
    }

}
